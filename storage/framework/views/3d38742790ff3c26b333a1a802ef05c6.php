        <p><strong>ConnectingNotes Mentor Policies and Guidelines</strong></p>
        <p><strong>Effective Date:</strong> April 12, 2025</p>
        <p><strong>Prepared by:</strong> ConnectingNotes Team</p>

        <h2>1. Overview</h2>
        <p>Welcome to ConnectingNotes! As a mentor, you play a vital role in shaping aspiring musicians.
            These guidelines outline expectations, responsibilities, and professional standards to ensure a safe,
            productive, and enriching experience for everyone.
        </p>

        <h2>2. Mentor Code of Conduct</h2>
        <ul>
            <li>Maintain professionalism and respect toward students, fellow mentors, and staff.</li>
            <li>Avoid any form of discrimination, harassment, or inappropriate behavior.</li>
            <li>Refrain from engaging in personal relationships that may affect the integrity of sessions.</li>
            <li>Use the platform only for educational and mentorship purposes.</li>
        </ul>

        <h2>3. Responsibilities</h2>
        <ul>
            <li>Conduct scheduled sessions punctually and regularly.</li>
            <li>Provide clear lesson plans and adapt to each student's learning pace.</li>
            <li>Track and update student progress on the platform.</li>
            <li>Submit reports and assessments on time.</li>
        </ul>

        <h2>4. Communication and Availability</h2>
        <ul>
            <li>Respond to student queries within 24–48 hours.</li>
            <li>Use the platform's messaging system for all official communications.</li>
            <li>Update your availability regularly to ensure smooth session bookings.</li>
        </ul>

        <h2>5. Session and Attendance Policy</h2>
        <ul>
            <li>Mentors are expected to attend all booked sessions unless an emergency arises.</li>
            <li>Cancellations must be notified at least 24 hours in advance.</li>
            <li>Three unexcused absences may result in temporary suspension or removal.</li>
        </ul>

        <h2>6. Data Privacy and Confidentiality</h2>
        <ul>
            <li>Keep student information confidential.</li>
            <li>Do not share any user data outside the platform.</li>
            <li>Follow data handling protocols set by ConnectingNotes.</li>
        </ul>

        <h2>7. Termination and Disciplinary Actions</h2>
        <ul>
            <li>Failure to comply with these guidelines may lead to investigation and disciplinary action.</li>
            <li>ConnectingNotes reserves the right to terminate mentor access for serious violations.</li>
        </ul>

        <h2>8. Acknowledgment</h2>
        <p>By continuing as a mentor, you agree to adhere to these policies and guidelines and uphold the values of
            ConnectingNotes.</p>
